"use strict";

const fs = require("fs");
const path = require("path");

const args = parseArgs(process.argv.slice(2));
const input = path.resolve(args.model || "models/ppo-agent.json");
const output = path.resolve(args.out || "models/ppo-browser-model.js");
const model = JSON.parse(fs.readFileSync(input, "utf8"));

if (model.type !== "NeuralPPOAgent") {
  throw new Error(`Browser export expects a NeuralPPOAgent model, got ${model.type}`);
}

fs.mkdirSync(path.dirname(output), { recursive: true });
fs.writeFileSync(
  output,
  `// Generated by scripts/export-browser-model.js\nwindow.COUP_PPO_MODEL = ${JSON.stringify(model)};\n`
);
console.log(`exported ${output}`);

function parseArgs(argv) {
  const out = {};
  for (let i = 0; i < argv.length; i++) {
    const arg = argv[i];
    if (!arg.startsWith("--")) continue;
    const key = arg.slice(2);
    const next = argv[i + 1];
    if (!next || next.startsWith("--")) out[key] = true;
    else out[key] = argv[++i];
  }
  return out;
}
